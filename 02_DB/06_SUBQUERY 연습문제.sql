SELECT EMP_ID, EMP_NAME, JOB_NAME, 
FLOOR((MONTHS_BETWEEN(SYSDATE, TO_DATE(SUBSTR(EMP_NO, 1, 6), 'RRMMDD')))/12) 나이 , 
TO_CHAR((SALARY * (1 + NVL(BONUS, 0)) ) * 12, 'L999,999,999') 보너스포함연봉
FROM EMPLOYEE MAIN 
JOIN JOB C ON ( MAIN.JOB_CODE = C.JOB_CODE )
WHERE EMP_NO  = (SELECT MAX(EMP_NO)	FROM EMPLOYEE SUB WHERE SUB.JOB_CODE = MAIN.JOB_CODE)  
ORDER BY 나이 DESC;










